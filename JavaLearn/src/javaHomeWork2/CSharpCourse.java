package javaHomeWork2;

public class CSharpCourse  extends Course{
	private boolean active;
	
	public CSharpCourse() {
		super();
	}

	public CSharpCourse(boolean active) {
		super();
		this.active = active;
	}

	public boolean getActive() {
		return active;
	}

	public void setActive(boolean active) {
		this.active = active;
	}
	
	

}
